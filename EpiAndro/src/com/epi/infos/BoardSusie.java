package com.epi.infos;

public class BoardSusie {
	private	String	title;
	private String	creneau_link;
	private Integer	timeline_barre;
	private	String	timeline_end;
	private	String	salle;
	private	String	intervenant;
	private	String	etat;
	private	String	type;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCreneau_link() {
		return creneau_link;
	}
	public void setCreneau_link(String creneau_link) {
		this.creneau_link = creneau_link;
	}
	public Integer getTimeline_barre() {
		return timeline_barre;
	}
	public void setTimeline_barre(Integer timeline_barre) {
		this.timeline_barre = timeline_barre;
	}
	public String getTimeline_end() {
		return timeline_end;
	}
	public void setTimeline_end(String timeline_end) {
		this.timeline_end = timeline_end;
	}
	public String getSalle() {
		return salle;
	}
	public void setSalle(String salle) {
		this.salle = salle;
	}
	public String getIntervenant() {
		return intervenant;
	}
	public void setIntervenant(String intervenant) {
		this.intervenant = intervenant;
	}
	public String getEtat() {
		return etat;
	}
	public void setEtat(String etat) {
		this.etat = etat;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
}
